package dbfs.data;

/**
 * Input message DTO.
 * @author maciej.wojciga
 *
 */
public class InputMessageData {
	// Sr000l000t000e0E
	private int rIM;
	private int lIM;
	private int tIM;
	private int eIM;
	
	public int getrIM() {
		return rIM;
	}
	public void setrIM(int rIM) {
		this.rIM = rIM;
	}
	public int getlIM() {
		return lIM;
	}
	public void setlIM(int lIM) {
		this.lIM = lIM;
	}
	public int gettIM() {
		return tIM;
	}
	public void settIM(int tIM) {
		this.tIM = tIM;
	}
	public int geteIM() {
		return eIM;
	}
	public void seteIM(int eIM) {
		this.eIM = eIM;
	}
	
}

